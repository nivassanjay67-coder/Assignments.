let colour = "Green"
switch(colour){
    case "red":
        console.log("Red is the color of energy and passion!");
        break;
    case "blue":
        console.log("Blue is calm and peaceful.");
        break;
    case "green":
        console.log("Green represents nature and growth.");
        break;
    case "yellow":
        console.log("Yellow is bright and cheerful!");
        break;
    case "purple":
        console.log("Purple is royal and mysterious.");
        break;
    default:
        console.log(`Wow! ${colour} is a unique choice!`);    
}